package com.kh.operator;

public class Ternary {

	public static void main(String[] args) {
		/* 6. 삼항연산자(Ternary) : 조건을 평가하고 세 가지 값 중 하나를 반환한다.(조건 ? 값1 : 값2) 형태로 사용.
		                         ex)나이가 20이상은 성인인가 미성년자인가 (삼항 = 일항 ? 이항 : 삼항)    */
		int age = 20;
		//              (   조건   ) ? (값1=T)  (값2=F)
		String status = (age >= 20) ? "성인" : "미성년자";
		System.out.println(status);
		
		int score = 85;
		String result = (score >= 60) ? "합격" : "불합격";
		System.out.println(result);
		
		int number = -5;
		String sign = (number > 0) ? "true" : "false";
		System.out.println(sign);
		
		int temperature = 25;
		String weather = (temperature > 21) ? "적당한 날씨" : "추운 날씨";
		System.out.println(weather);
		
		//숫자 7의 값을 짝수인지 홀수인지 출력
		int num = 7;
		String evenOrOdd = (num%2 == 0) ? "짝수" : "홀수";
		System.out.println(evenOrOdd);
		
		int num1 = -5;
		String sign2 = (num1 > 0) ? "양수" : (num1 < 0) ? "음수" : "0";
		System.out.println(sign2);
	}

}
